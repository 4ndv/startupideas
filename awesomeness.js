$(document).ready(function(){
  var generate = function(){
    generateOne();
    generateTwo();
  }

  var generateOne = function(){
    hideHello();
    $("#part-one").fadeOut(150, function(){
      $(this).text(variants.what[Math.floor(Math.random()*variants.what.length)]);
      social();
      $(this).fadeIn(250);
    });
    hideHello();
  }

  var generateTwo = function(){
    hideHello();
    $("#part-two").fadeOut(150, function(){
      $(this).text(variants.with[Math.floor(Math.random()*variants.with.length)]);
      social();
      $(this).fadeIn(250);
    });
  }

  var hideHello = function(){
    $("#hello").fadeOut(150);
  }

  var social = function(){
    var rand = Math.floor(Math.random()*10000000);
    var text = "Моя идея стартапа: " + $("#part-one").text() + " " + $("#part-two").text() + ".";
    $("#tweet").attr("href", "https://twitter.com/home?status=" + text + " http://git.io/vqU3L");
    $("#vkshare").attr("href", "https://vk.com/share.php?url=http://4ndv.github.io/startupideas/?" + rand + "&title=Генератор идей стартапов. " + text + " Получи свою здесь!" + "&description=Генератор идей стартапов v3.0")
  }

  $("#generate").click(generate);
  $("#part-one").click(generateOne);
  $("#part-two").click(generateTwo);
});
